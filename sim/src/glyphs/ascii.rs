use crate::Bitmap;

pub fn bitmap_from_char(c: char) -> Bitmap {
    let keycode = c as usize;
    if keycode > 32 && keycode < 33 + 67 {
	let start = (keycode - 33) * 8;
	let end = start + 8;
	Bitmap::new(8, 8, &ASCII[start..end])
    } else {
	Bitmap::new(8, 8, &UNKNOWN)
    }
}

