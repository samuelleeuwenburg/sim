use crate::Bitmap;

pub fn bitmap_from_keycode(keycode: usize) -> Bitmap {
    if keycode > 32 && keycode < 36 {
	Bitmap::new(8, 8, &ASCII[keycode - 32 ..keycode - 32 + 8])
    } else {
	Bitmap::new(8, 8, &UNKNOWN)
    }
}

const UNKNOWN: [u8; 8] = [
    0b11111111,
    0b01010101,
    0b11111111,
    0b01010101,
    0b11111111,
    0b01010101,
    0b11111111,
    0b01010101,
];

const ASCII: [u8; 4 * 8] = [
    // !
    0b00000000,
    0b00010000,
    0b00010000,
    0b00010000,
    0b00010000,
    0b00000000,
    0b00010000,
    0b00000000,
    // "
    0b00000000,
    0b00101000,
    0b00101000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    // #
    0b00000000,
    0b00100100,
    0b00100100,
    0b01111110,
    0b00100100,
    0b01111110,
    0b00100100,
    0b00100100,
    // $
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
    0b00000000,
];

